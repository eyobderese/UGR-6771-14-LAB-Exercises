void main() {
  int DAY = 1;

  switch (DAY) {
    case 1:
      print("Monday");
    case 2:
      print("Tuesday");
    case 3:
      print("Wednesday");
    case 4:
      print("Thursday");
    case 5:
      print("Friday");
    case 6:
      print("Saturday");

    default:
      print("Sunday");
  }
}
